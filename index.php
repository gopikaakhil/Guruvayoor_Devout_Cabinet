				<p>This is conducted in the month of Kumbha (Feb-March)
				and lasts for 10 days. The hoisting of the temple flag atop 
				the flagstaff (Dwajastambham) about 70 feet high heralds the festival.
				On the 1st day, an elephant race is held.
				On the next 6 days, there are elephant processions in the morning, afternoon and night.
				In the morning, there is the Sribhuta Bali (Offering of oblation at various altar).
				
