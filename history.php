	<div class="maha" style=" outline-style: double;outline-color: #428a78;outline-width: 20px;">
	<center><h2>Guruvayur Mahatmyam – early legends of Guruvayur temples</h2></center>
	<p style="color:black;font-weight:bold;"> Sri Naradiya Purana mentions how Janamejaya was cured of leprosy by taking refuge under the feet of Guruvayuruppan. The Pandavas handed over the kingdom to their grandson Parikshit, and left for the forest to spend their last days. Parikshit died of the curse of a saint, who cursed that Parikshit will die of snake bite by Takshaka, the king of serpents. After the death of Parikshit he was succeeded by his son Janamejaya. Janamejaya conducted a sacrifice to destroy all the snakes of the world including Takshaka, who was the cause of his father's death . Hundreds of thousands of snakes fell into the sacrificial fire and were killed, but the sacrifice was stopped by a Brahmin called Astika, before Takshaka was killed.[11]

Since Janamejaya was responsible for the death of millions of snakes, he was afflicted with leprosy. He lost all hope of a cure. One day Sage Atreya (son of Atri) came before Janamejaya and told him to take refuge under the feet of Krishna at Guruvayoor. Atreya told him that in the temple at Guruvayur the effulgence of Sri Hari is at its best and Vishnu showers his blessings on all devotees. He immediately rushed there and spent the next ten months worshipping the God at Guruvayur. At the end of ten months, he returned home hale and hearty and took the astrologer to task for making a false prediction. The astrologer told him that he would find the mark of a snakebite on his left leg. He had escaped death only because he was at that time in a temple where Anantha (the emperor of serpents) was present and Anantha was the brother to the God at Guruvayur where he had finished worshipping.[11]

The king being convinced of the astrologer's verdict decided to build a full-fledged temple at Guruvayur. In course of time, this temple came to be downgraded and reduced to poverty during the days when Kerala was ruled by the Perumals. The Perumal rulers were mostly Shaivites and did not extend their patronage to Vaishnavite shrines. The Shiva temple at Mammiyur received their patronage and with the shift of royal patronage, the worshippers also swung more to the Shiva temple. The Guruvayur temple was thus reduced to utmost poverty, without even means to light the temple lamp. However, one day, a holy man went to the Mammiyur temple for food and hospitality for the night. Though the temple was affluent, the temple authorities pretended they had nothing and scornfully directed him to the neighbouring Guruvayur temple knowing fully well that the temple was in destitute conditions. When the holy man entered the precincts of that temple, he was courteously received by a brahmin boy and sumptuously fed. The holy man was very much pleased and he pronounced a blessing. "I came here from Mammiyur temple, because they said there is nothing there," he said."They told me also that there is plenty here. Well hereafter it will be exactly like that!" It is said from that day, Mammiyur Siva temple began to decline, and the fortunes of Guruvayur Vishnu temple progressed from strength to strength. But now, Mammiyur is also progressing by the number of devotees.</p>
	
