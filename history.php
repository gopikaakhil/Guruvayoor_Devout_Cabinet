	<center><h2>Stories surrounding the Guruvayur Temple tank</h2></center>
	<p style="color:black;font-weight:bold;"> 
Guruvayur Temple tank (pond)
The temple tank (pond) on the northern side of the temple is called Rudratheertham. The legend say that for thousands of years, Lord Shiva and his family worshipped Lord Vishnu on the southern bank of this pond, the current location of the temple, and as Shiva is also known by the name 'Rudra', the pond came to be known as Rudratheertham.

It is said that in the ancient days, the pond extended up to Mammiyur and Thamarayur (about 3 km away from the Temple) and was known for its ever blooming magical lotus flowers. Pracetas (the ten sons of mythical King Prajinabarhis and his Queen Suvarna) came to this place to do undergo a tapasya to become "the king of all kings" with the help of god Vishnu. Sensing the motive of the Pracetas, Siva emerged from the sacred tank and revealed to them the "Rudragitam", a hymn in praise of Vishnu. Siva suggested them to chant the hymn with all their heart to get their wishes fulfilled. The princes won the favour of Vishnu after rigorous tapasya for 10,000 years on the banks of the tank after bathing on it by chanting the hymns.</p>
