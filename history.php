	<div class="colo" style=" outline-style: double;outline-color: #428a78;outline-width: 20px;">
	<center><h2>Colonial history</h2></center>
	<p style="color:black;font-weight:bold;"> Mamankam was a very famous event at Thirunavaya, on the bank of Bharathappuzha . The war between the Zamorins and the Raja of Valluvanad of Thirunavaya in a way popularised Guruvayur temple. Due to the prolonged war people across the river bank started preferring Guruvayur. Even the Zamorin become a devotee and thus his subjects followed him completely . The central shrine which we see today is said to have been rebuilt in 1638 AD. Vishwabali was performed later to propitiate all the spirits, good and bad. By the end of 16th century Guruvayur had become most popular pilgrimage centre in Kerala.

In 1716 AD, the Dutch raided Guruvayur. They looted treasures, gold of the flag staff, and set fire to the Western Gopuram. It was later rebuilt in 1747 AD. In 1755AD, the Dutch in war with the Zamorin destroyed Trikkunavay temple and the Brahmins fled from there. Later the Zamorin become the trustee of both Guruvayur and Trikkunavay, and also their Melkoyma (Sovereign protector).

In 1766 AD, Hyder Ali of Mysore captured Kozhikkode (Calicut) and then Guruvayur. He fined 10,000 fanams to spare the temple . This fine was paid but due to insecurity pilgrims receded, the supply of rice was stopped and the tenants stopped annual dues. On the request of the Malabar Governor, Shrnivasa Rao, Hyder Ali granted a Devadaya (free gift) and the temple was saved from extinction. Again in 1789 AD Tippu Sultan invaded Zamorin’s province. Apprehending the destruction, the idol was hidden underground and the Utsava vigraha was taken to Ambalapuzha by Mallisseri Namboodiri and Kakkad othikkan. Tippu destroyed the smaller shrines and set fire to the Temple, but it was saved due to timely rain. Tippu lost to the Zamorin and the English in 1792 AD. The idol hidden underground and the Utsava vigraha were re-installed on 17 September 1792. But the daily poojas and routines were seriously affected. [8]

Ullanad Panikkars rescued and looked after the temple from 1825 to 1900. Like Chempakasseri Nambudiri and Desavarma Nambudiri, Panikkars offered everything from service to property. Thus with their help daily puja and annual festivals were once again restored. From 1859 to 1892, the Chuttambalam, the Vilakkumatam, the Koottambalam and Sasta shrine were renovated and roofed with copper sheeting. In 1900, Konti Menon, as a manager fixed the hours of worship and led the drive to keep the temple premises clean. He set up the big bell and reconstructed Pattayappura (granary). In 1928, Kozhikode once again became the administrator of Guruvayur</p>
	</p></div>
	
