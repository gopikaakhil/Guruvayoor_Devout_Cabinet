	<center><h2>Post-olonial history</h2></center>
	<p style="color:black;font-weight:bold;">Fire of 1970 <br>

The Vilakkumatham
On 30 November 1970, a massive fire broke out in the temple. It began from the western Chuttambalam (circumambulatory temple) and raged all around for five hours, but the Sreekovil (sanctum sanctorum), the main deity, sub-shrines of Ganesha, Ayyappa, Bhagavati and flagstaff remained unaffected. This shocking incidence of fire took place on 29 November during the season of Ekadasi Lighting. The day was celebrated on a grand scale and all the lamps in the Vilakkumatham (a frame on the outside a Sanctum Sanctorum of a temple covered with oil lamps in rows from top to bottom) with wher sanwere lighted. After last Siveli procession, the temple and the gates of Gopuram were closed for the day. Around 1.00am, somebody in the immediate neighbourhood near the western Chuttambalam saw a blaze from within the Temple. Roused by the news, a lot local people, rushed to the Temple and joined in fighting the fire with water and sand. Later, the fire force units of Ponnani, Trichur and Fertilisers and Chemicals Travancore arrived and started fighting the fire which seems to have started from the western Vilakkumatam. It was brought under control by 5.30 am. Seeing the uncontrollable fire, the authorities had already removed the valuables from the Sreekovil. The Ganapathy deity, Sastha deity and the main deity of God Guruvayurappan were shifted to the Koothambalam and then to a safer place, the residence of the Tantri. The fire gutted the whole of chuttambalam, the entire Vilakkumatam on the west, south and north sides. Even though it was only 3 yards from the Chuttambalam, the fierce fire did not touch the Sreekovil. </p>
	
