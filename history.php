	<center><h2>The Elephant</h2></center>
	<p style="color:black;font-weight:bold;">
Devotees at Guruvayur Temple
Strict dress code exists for people who wish to enter the Guruvayur Temple. Men are to wear mundu around their waist, without any dress covering their chest. But it is allowed to cover the chest region with a small piece of cloth (veshthi). Boys are allowed to wear shorts, but they are also prohibited from wearing a shirt. Girls and women are not allowed to wear any trouser like dresses or short skirts. Women are allowed to wear sari and girls are to wear long skirt and blouses. Presently the dress code for women have been relaxed with shalwar kameez (chudidhar pyjamas) being allowed.[14]

Temple Elephants
The Punnathur kotta elephant yard is the tethering site or home of the 56 elephants belonging to the temple. Also called the Anakotta (Elephant Fort in vernacular language), the place is the home to the largest population of captive male Asian elephants. These elephants are donated by devotees to the temple, and often they prefer donating tusked male elephants and hence the skewed ratio of males to females.[15] The elephants were initially kept at a compound close to the temple when the numbers were low. However, with more devotees donating elephants, the space became insufficient and hence was moved to a larger property three kilometres away from the temple. Because of their association with the temple, devotees consider several of these elephants as living forms of Lord Guruvayurappa himself.[16] Guruvayur Keshavan is the most famous among those, who, even four decades after his demise is the talk of the town.[17] To honour the royal one, every year on his death anniversary, homage is paid to a life-sized statue of the late tusker followed by a grand feast to the elephants <br>
